    private void updatingUserList(){
        DefaultListModel data = new DefaultListModel();
        
        for(int i = 0; i< list.size(); i++){
            Client client = list.get(i);
            data.addElement(client.fullName());
        }
        
        this.listClients.setModel(data);
    }
    
