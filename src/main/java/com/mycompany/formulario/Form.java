    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed

        Client client = new Client();
        client.setName(this.inputNombre.getText());
        client.setLastname(this.inputLastname.getText());
        client.setEmail(this.inputEmail.getText());
        client.setPhone(this.inputPhone.getText());
        list.add(client);
        updatingUserList();
        JOptionPane.showMessageDialog(rootPane, "El cliente ha sido guardado correctamente");
        cleanUp();
    }//GEN-LAST:event_btnSendActionPerformed
    
    private void cleanUp(){
        this.inputNombre.setText("");
        this.inputLastname.setText("");
        this.inputEmail.setText("");
        this.inputPhone.setText("");
    }
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int indexUserSelected = this.listClients.getSelectedIndex();
        list.remove(indexUserSelected);
        updatingUserList(); 
        JOptionPane.showMessageDialog(rootPane, "Se eliminó correctamente ");
    }//GEN-LAST:event_btnDeleteActionPerformed

